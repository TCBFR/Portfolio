	DROP DATABASE IF EXISTS sio; 
	CREATE DATABASE sio;
	DROP USER IF EXISTS 'sio'@'%'; 
	CREATE USER 'sio'@'%' IDENTIFIED BY 'sio'; 
	GRANT ALL PRIVILEGES ON sio.* TO 'sio'@'%'; 
	FLUSH PRIVILEGES;
	USE sio;
	DROP TABLE IF EXISTS news;

	CREATE TABLE `news` (
		`knews` INT NOT NULL AUTO_INCREMENT,
		`title` VARCHAR(255) NOT NULL,
		`body` TEXT NOT NULL ,
		`created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
		`updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
		PRIMARY KEY (`knews`)
	) ENGINE = InnoDB;
	CREATE TABLE `utilisateur`(
		`kutilisateur` INT NOT NULL AUTO_INCREMENT,
		`Nom` VARCHAR(255) NOT NULL,
		`Prenom` VARCHAR(255) NOT NULL,
		`demande` TEXT NOT NULL,
		`created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
		`updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
		PRIMARY KEY (`kutilisateur`)
	) ENGINE = InnoDB;

	CREATE TABLE `articles`(
		`karticles` INT NOT NULL AUTO_INCREMENT,
		`title` VARCHAR(255) NOT NULL,
		`body` TEXT NOT NULL,
		`created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
		`updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
		PRIMARY KEY (`karticles`)
	) ENGINE = InnoDB;